        String kode = inputKode.getText();
        String nama = inputNama.getText();
        String kamar = inputKamar.getText();
        
        inputKode.setText("");
        inputNama.setText("");
        inputKamar.setText("");
        
        DBconnect connect = new DBconnect();
        connect.setDataBank(kode, nama, kamar);
        
        DefaultTableModel model;
        model = (DefaultTableModel) tblHasil.getModel();
        
