    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tb;
        tb = (DefaultTableModel) tblHasil.getModel();
        
        String kode = inputKode.getText();
        String nama = inputNama.getText();
        String kamar = inputKamar.getText();
        tb.setValueAt(kode, selectedRow, 0);
        tb.setValueAt(nama, selectedRow, 1);
        tb.setValueAt(kamar, selectedRow, 2);
        
        DBconnect connect = new DBconnect();
        connect.updateBank(nama, kamar, tb.getValueAt(selectedRow, 0).toString());
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tb;
        tb = (DefaultTableModel) tblHasil.getModel();
        int kd = Integer.parseInt(tb.getValueAt(selectedRow, 0).toString());
        
        for(int i = selectedRow; i < index-1; i++){
            tb.setValueAt(tb.getValueAt(i + 1, 0), i, 0);
            tb.setValueAt(tb.getValueAt(i + 1, 1), i, 1);
            tb.setValueAt(tb.getValueAt(i + 1, 2), i, 2);
        }
        
        tb.setValueAt("", index-1, 0);
        tb.setValueAt("", index-1, 1);
        tb.setValueAt("", index-1, 2);
        index--;
        
        inputKode.setText("");
        inputNama.setText("");
        inputKamar.setText("");
        
        DBconnect connect = new DBconnect();
        connect.deleteBank(kd);
    }//GEN-LAST:event_btnDeleteActionPerformed

